public class Exemple03 {
  public static void main(String[] args) {
    
    int var;
    double x;

    var = 10;
    x = 10.0;

    System.out.println("Original valor de var " + var);
    System.out.println("Original valor de x " + x);
    System.out.println();
    
    var = var / 4;
    x = x / 4;
    
    System.out.println("var com divisão " + var);
    System.out.println("x com divisão " + x);
    
    
  }
}
